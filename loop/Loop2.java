package loop;

public class Loop2 {

	public static void main(String[] args) {
		

	    // outer loop
	    for (int i=0; i<=5  ; ++i) {
	    
	      for (int j = 1; j <= i; ++j) {
	        System.out.print("@ ");
	      }
	      System.out.println("@");
	    }
	}
		
	}


